BienA = 9
BienB = 12

BienC = "Hello"

#Điều kiện kết hợp 'và'
#True khi tất cả các điều kiện thành phần là True
if BienA < BienB and BienC == "hello": # Nếu BienA nhỏ hơn BienB Và BienC == "hello"
    print("hi1")

#Điều kiện kết hợp 'hoặc'
#True khi 1 trong các điều kiện thành phần là True
if BienA < BienB or BienC == "hello": # Nếu BienA nhỏ hơn BienB hoặc BienC == "hello"
    print("hi2")

